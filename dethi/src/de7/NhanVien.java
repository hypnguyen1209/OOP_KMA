package de7;

public class NhanVien {
    private int id;
    private String hoten;

    public NhanVien(int id, String hoten) {
        this.id = id;
        this.hoten = hoten;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getHoten() {
        return hoten;
    }

    public void setHoten(String hoten) {
        this.hoten = hoten;
    }
    
    
}
