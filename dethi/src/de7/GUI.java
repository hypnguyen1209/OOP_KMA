    public Boolean checkNull() {
        if(txthoTen1.getText().equals("")) return true;
        if(txtMaGV.getText().equals("")) return true;
        if(txtDC.getText().equals("")) return true;
        if(!nam.isSelected() && !nu.isSelected()) return true;
        return false;
    }
    
    private void btnLuuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLuuMouseClicked
        if(checkNull()) {
            JOptionPane.showMessageDialog(null, "Không ô nào đc đê trống.");
            return;
        }
        if(checkExist(txtMaGV.getText())) {
            JOptionPane.showMessageDialog(null, "Đã tồn tại.");
            return;
        }
        String hoten = txthoTen1.getText();
        String ma = txtMaGV.getText();
        String dc = txtDC.getText();
        String gioiTinh = nam.isSelected() ? "nam" : "nữ";
        String khoa = cbKhoa.getSelectedItem().toString();
        GiangVien gv = new GiangVien(++id, ma, dc, gioiTinh, khoa, hoten);
        try {
            gv.writeToFile(gv);
            JOptionPane.showMessageDialog(null, "Lưu thành công.");
            clearInfo();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Lưu thất bại.");
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnLuuMouseClicked

