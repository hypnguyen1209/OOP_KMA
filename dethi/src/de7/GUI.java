    public void setId() {
        int lines = 0;
        FileReader fw = null;
        try {
            fw = new FileReader("giangvien.dat");
            BufferedReader reader = new BufferedReader(fw);
            while (reader.readLine() != null) lines++;
        } catch (IOException e) {
            e.printStackTrace();
        }
        this.id = lines;
    } 
    public Boolean checkExist(String s) {
        File file = null;
        try {
            file = new File("giangvien.dat"); 
            FileReader fr = new FileReader(file);  
            BufferedReader br = new BufferedReader(fr); 
            StringBuffer sb = new StringBuffer(); 
            String line;
            while((line = br.readLine())!= null) {
                String[] gv = line.split("\\$");
                if (s == gv[2]) {
                    return true;
                }
            }  
            return false;
        } catch(Exception ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
