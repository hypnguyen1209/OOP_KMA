    public Boolean checkNull() {
        if(tfMaSV.getText().equals("")) return true;
        if(tfHoTen.getText().equals("")) return true;
        if(tfNgaySinh.getText().equals("")) return true;
        if(!nam.isSelected() && !nu.isSelected()) return true;
        if(tfDiemTB.getText().equals("")) return true;
        if(svATTT.isSelected()) {
            if(tfHocPhi.getText().equals("")) return true;
        } else {
            if(tfLuong.getText().equals("")) return true;  
            if(tfDonVi.getText().equals("")) return true;
        }
        return false;
    }
    
