    private void saveInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveInfoActionPerformed
        // TODO add your handling code here:
        if(this.checkNull()) {
            JOptionPane.showMessageDialog(null, "Vui long nhap du thong tin");
            return;
        }
        if(svATTT.isSelected()) {
            SinhvienATTT svAttt;
            try {
                svAttt = new SinhvienATTT(tfMaSV.getText(),
                        tfHoTen.getText(),
                        tfNgaySinh.getText(),
                        nam.isSelected() ? "nam" : "nu",
                        Float.parseFloat(tfDiemTB.getText()),
                        Float.parseFloat(tfHocPhi.getText()));
                FileWriter fw = null;
                try {
                    fw = new FileWriter("svat.dat");
                    BufferedWriter bw = new BufferedWriter(fw);
                    bw.append("Ma SV: " + svAttt.getMaSV() + "\n" + 
                            "Ho ten: " + svAttt.getHoTen() + "\n" + 
                            "Ngay sinh: " + svAttt.getNgaySinh() + "\n" + 
                            "Gioi tinh: " + svAttt.getGioiTinh() + "\n" +
                            "Diem TB: " + svAttt.getDiemTB() + "\n" + 
                            "Hoc phi: " + svAttt.getHocPhi() + "\n");
                    bw.newLine();
                    bw.close();
                    fw.close();
                    JOptionPane.showMessageDialog(null, "Luu du lieu thanh cong");
                } catch(Exception ex) {
                    JOptionPane.showMessageDialog(null, "Luu file that bai");
                    Logger.getLogger(Form3.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch(NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, ex);
                return;
            }
        } else {
            SinhvienMatMa svMM;
            try {
                svMM = new SinhvienMatMa(tfMaSV.getText(),
                        tfHoTen.getText(),
                        tfNgaySinh.getText(),
                        nam.isSelected() ? "nam" : "nu",
                        Float.parseFloat(tfDiemTB.getText()),
                        tfDonVi.getText(),
                        Float.parseFloat(tfLuong.getText()));
                FileWriter fw = null;
                try {
                    fw = new FileWriter("svmm.dat");
                    BufferedWriter bw = new BufferedWriter(fw);
                    bw.append("Ma SV: " + svMM.getMaSV() + "\n" + 
                            "Ho ten: " + svMM.getHoTen() + "\n" + 
                            "Ngay sinh: " + svMM.getNgaySinh() + "\n" + 
                            "Gioi tinh: " + svMM.getGioiTinh() + "\n" +
                            "Diem TB: " + svMM.getDiemTB() + "\n" + 
                            "Don vi: " + svMM.getDonVi() + "\n" + 
                            "Luong: " + svMM.getLuong());
                    bw.newLine();
                    bw.close();
                    fw.close();
                    JOptionPane.showMessageDialog(null, "Luu du lieu thanh cong");
                } catch(Exception ex) {
                    JOptionPane.showMessageDialog(null, "Luu file that bai");
                    Logger.getLogger(Form3.class.getName()).log(Level.SEVERE, null, ex);
                }
            } catch(NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, ex);
                return;
            }
        }
    }//GEN-LAST:event_saveInfoActionPerformed

