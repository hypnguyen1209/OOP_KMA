    public Boolean checkNull() {
        if(tfHoTen.getText().equals("")) return true;
        if(tfNgaySinh.getText().equals("")) return true;
        if(tfDiaChi.getText().equals("")) return true;
        if(!nam.isSelected() && !nu.isSelected()) return true;
        if(tfHSL.getText().equals("")) return true;
        if(tfThamNien.getText().equals("")) return true;
        if(tfLuong.getText().equals("")) return true;   
        return false;
    }
    
    public void initShow() {
        try {
            File file = new File("nhanvien.dat"); 
            FileReader fr = new FileReader(file);  
            BufferedReader br = new BufferedReader(fr); 
            StringBuffer sb = new StringBuffer(); 
            String line;
            while((line = br.readLine())!= null) {
                String[] nv = line.split("\\$");
                this.model.addRow(nv);
                nvs.add(new NhanVien(nv[0], nv[1], nv[2], nv[3], nv[4], Float.parseFloat(nv[5]), Integer.parseInt(nv[6]), Float.parseFloat(nv[7])));
            }  
        } catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Show thong tin that bai");
            Logger.getLogger(Form2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
