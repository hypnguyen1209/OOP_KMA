    private void saveDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveDataActionPerformed
        // TODO add your handling code here:
        if(this.checkNull()) {
            JOptionPane.showMessageDialog(null, "Vui long nhap du thong tin");
            return;
        }
        NhanVien nv;
        try {
        nv = new NhanVien(tfHoTen.getText(), 
                tfNgaySinh.getText(),
                tfDiaChi.getText(), 
                nam.isSelected() ? "nam" : "nu", 
                cbPhongBan.getSelectedItem().toString(), 
                Float.parseFloat(tfHSL.getText()),
                Integer.parseInt(tfThamNien.getText()),
                Float.parseFloat(tfLuong.getText()));
        } catch(NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, ex);
            return;
        }
        FileWriter fw = null;
        try {
            fw = new FileWriter("nhanvien.dat");
            BufferedWriter bw = new BufferedWriter(fw);
            nvs.add(nv);
            for (NhanVien n: nvs) { 		      
                bw.append(n.getHoTen() + "$" +
                    n.getNgaySinh() + "$" + 
                    n.getDiaChi() + "$" + 
                    n.getGioiTinh() + "$" + 
                    n.getPhongBan() + "$" + 
                    n.getHeSoLuong() + "$" + 
                    n.getThamNien() + "$" +
                    n.getLuong() + "$" +
                    n.luongTL());
                bw.newLine();
            }
            bw.close();
            fw.close();
            this.model.addRow(new String[]{
                nv.getHoTen(),
                nv.getNgaySinh(),
                nv.getDiaChi(),
                nv.getGioiTinh(), 
                nv.getPhongBan(),
                nv.getHeSoLuong().toString(), 
                Integer.toString(nv.getThamNien()),
                nv.getLuong().toString(),
                nv.luongTL().toString()
            });
            JOptionPane.showMessageDialog(null, "Luu du lieu thanh cong");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Luu file that bai");
            Logger.getLogger(Form2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_saveDataActionPerformed

